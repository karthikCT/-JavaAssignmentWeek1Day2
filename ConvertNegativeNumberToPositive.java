package week1.day2;

public class ConvertNegativeNumberToPositive {

	public static void main(String[] args) {
		int num = -40;
		if (num < 0)
		{
		int	sum = num*-1;
			System.out.println("The number -40 is converted to "+sum);
			
		}
		else
		{
			System.out.println("The number is not negative number");
		}
		
	}

}
